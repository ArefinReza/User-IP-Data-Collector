document.addEventListener("DOMContentLoaded", () => {
    // Generate or fetch the session ID from the server
    const sessionId = "<?= session_id(); ?>"; // This should be generated by PHP
    const startTime = Date.now();

    // Function to send end time and total time spent to the server
    const sendEndTime = () => {
        const endTime = Date.now();
        const totalTimeSpent = Math.round((endTime - startTime) / 1000); // Time in seconds

        // Use sendBeacon for a reliable data transfer even when the user leaves the page
        navigator.sendBeacon(
            "store_end_time.php",
            new URLSearchParams({
                session_id: sessionId,
                total_time_spent: totalTimeSpent,
            })
        );
    };

    // Add event listeners to capture when the user leaves the page
    window.addEventListener("beforeunload", sendEndTime);
    document.addEventListener("visibilitychange", () => {
        if (document.visibilityState === "hidden") {
            sendEndTime();
        }
    });
});
